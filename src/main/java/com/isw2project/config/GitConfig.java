package com.isw2project.config;

public class GitConfig {

    private String repoDir;
    private String codeOutputDir;

    public String getRepoDir() { return repoDir; }
    public void setRepoDir(String repoDir) { this.repoDir = repoDir; }

    public String getCodeOutputDir() { return codeOutputDir; }
    public void setCodeOutputDir(String codeOutputDir) { this.codeOutputDir = codeOutputDir; }
}